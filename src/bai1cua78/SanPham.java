package bai1cua78;

public class SanPham {
	private int MaSP;
	private String TenSP;
	private double giacaSP;
	private String xuatsuSP;
	public int getMaSP() {
		return MaSP;
	}
	public void setMaSP(int maSP) {
		MaSP = maSP;
	}
	public String getTenSP() {
		return TenSP;
	}
	public void setTenSP(String tenSP) {
		TenSP = tenSP;
	}
	public double getGiacaSP() {
		return giacaSP;
	}
	public void setGiacaSP(double giacaSP) {
		this.giacaSP = giacaSP;
	}
	public String getXuatsuSP() {
		return xuatsuSP;
	}
	public void setXuatsuSP(String xuatsuSP) {
		this.xuatsuSP = xuatsuSP;
	}
	
}
